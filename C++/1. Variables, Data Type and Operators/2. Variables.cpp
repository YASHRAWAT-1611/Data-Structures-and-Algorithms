#include <iostream>
using namespace std;

int main()
{
    int a = 10; /* It stores integer type values*/
    float b = 190.99; /* It stores decimal numbers with 4 Bytes of storage*/
    double c = 169.28; /* It Stores decimal numbers with 8 Bytes of storage*/
    char d = 'Y'; /* It stores single character/letter/number, or ASCII values*/
    bool Yes = true; /* It stores true or flase wavle*/
    bool No = false;

    cout << "The integer value in a is " << a << endl;
    cout << "The float value in b is " << b << endl;
    cout << "The double value in c is " << c << endl;
    cout << "The char value in d is " << d << endl;
    cout << "The bool value in Yes is " << Yes << endl;
    cout << "The bool value in No is " << No << endl;

    return 0;
}